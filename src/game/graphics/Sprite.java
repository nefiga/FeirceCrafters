package game.graphics;

public class Sprite {
    public final int startX;
    public final int startY;
    public final int width;
    public final int height;

    public Sprite(int startX, int startY, int width, int height) {
        this.startX = startX;
        this.startY = startY;
        this.width = width;
        this.height = height;
    }
}
